FROM ppty_dtl,
     police_case,
     PERSON,
     detent
WHERE 1 = 1
  AND ppty_dtl.pol_case_id = 1000028158
  AND ppty_dtl.pol_case_id = police_case.pol_case_id
  AND PERSON.PERSON_ID = DETENT.PERSON_ID
  AND PPTY_DTL.DETENT_ID = DETENT.DETENT_ID
ORDER BY FULL_NAME;



